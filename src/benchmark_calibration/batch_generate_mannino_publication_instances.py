# -*- coding: utf-8 -*-
"""
batch_generate_mannino_publication_instances.py

Generate OR--ICU--ward benchmark instances using Mannino/SINTEF surgery
duration data as the OR-duration source.

Important:
- Mannino/SINTEF provides surgery-team mix and empirical surgery durations.
- It does NOT provide ICU LOS, ward LOS, ICU-ready dates, or transfer-blocking records.
- Therefore, this script injects Mannino surgery durations while preserving the
  downstream LOS fields generated by the existing benchmark generator.

Recommended pilot:
    python batch_generate_mannino_publication_instances.py ^
      --mannino-pool-dir mannino_prepared ^
      --output-root mannino_publication_batch_instances ^
      --sizes 70 ^
      --seeds 7 ^
      --scenarios nominal transfer_bottleneck ^
      --mode duration_only
"""

from __future__ import annotations

import argparse
import json
import math
from pathlib import Path
from typing import Any, Dict, Optional

import pandas as pd

from publication_experiment_config import (
    PUBLICATION_BED_SETTINGS,
    PUBLICATION_PRIORITY_POLICY,
    PUBLICATION_PRIORITY_SHARES,
    PUBLICATION_HEPATOBILIARY_PROXY,
    PUBLICATION_SIZES,
    PUBLICATION_SEEDS,
    PUBLICATION_SCENARIOS,
    PUBLICATION_EXPERIMENT_VERSION,
)
from shehadeh_style_benchmark_generator_v3 import (
    BenchmarkConfig,
    generate_benchmark,
    extend_instance_with_blocking_states,
    extend_instance_with_synthetic_inputs,
    save_instance,
    run_extended_sanity_checks,
)


def _read_pool(pool_dir: Path, n: int, seed: int) -> pd.DataFrame:
    p = pool_dir / f"mannino_pool_n{n}_seed{seed}.csv"
    if not p.exists():
        raise FileNotFoundError(
            f"Missing Mannino pool file: {p}\n"
            f"Generate it first with generate_mannino_patient_pool.py."
        )
    df = pd.read_csv(p)
    required = {"team", "duration_min"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"{p} is missing columns: {missing}")
    if len(df) != int(n):
        raise ValueError(f"{p} has {len(df)} rows, but n={n}. Regenerate the pool.")
    return df.reset_index(drop=True)


def _auto_team_map(pool: pd.DataFrame, blocks: pd.DataFrame) -> Dict[str, str]:
    team_order = pool["team"].astype(str).value_counts().index.tolist()
    spec_order = blocks["specialty"].astype(str).value_counts().index.tolist()
    if not spec_order:
        raise ValueError("blocks.csv has no specialty values.")
    return {team: spec_order[i % len(spec_order)] for i, team in enumerate(team_order)}


def _compatible_block_string(blocks: pd.DataFrame, specialty: str) -> str:
    ids = (
        blocks[blocks["specialty"].astype(str) == str(specialty)]["block_id"]
        .astype(int)
        .sort_values()
        .tolist()
    )
    return ",".join(str(x) for x in ids)


def _refresh_type_stats_preserve_downstream(inst: Dict[str, Any], old_type_stats: pd.DataFrame) -> pd.DataFrame:
    """Rebuild type_stats after Mannino duration injection.

    The original generator's downstream routines expect columns such as
    mu_icu and mu_ward in type_stats. A previous version of this script replaced
    type_stats with duration-only statistics, causing KeyError: 'mu_ward'.

    This function updates duration summaries but preserves all downstream LOS
    columns from the original type_stats whenever possible.
    """
    surgeries = inst["surgeries"].copy()
    new_duration = (
        surgeries.groupby("specialty")["duration_min"]
        .agg(["count", "mean", "std"])
        .reset_index()
        .rename(columns={"count": "count", "mean": "mu_duration", "std": "sd_duration"})
    )
    new_duration["sd_duration"] = new_duration["sd_duration"].fillna(0.0)

    old = old_type_stats.copy()
    # Identify the specialty column in old type_stats.
    spec_col = None
    for c in ["specialty", "type", "surgery_type"]:
        if c in old.columns:
            spec_col = c
            break

    if spec_col is None:
        # Conservative fallback: do not overwrite old type_stats.
        return old

    if spec_col != "specialty":
        old = old.rename(columns={spec_col: "specialty"})

    # Drop old duration-like columns before merging new duration moments.
    duration_like = [
        c for c in old.columns
        if c.lower() in {
            "count", "n", "mu_duration", "sd_duration", "duration_mean",
            "duration_sd", "mean_duration", "std_duration"
        }
    ]
    old_keep = old.drop(columns=duration_like, errors="ignore")

    merged = old_keep.merge(new_duration, on="specialty", how="right")

    # Fill missing downstream LOS columns if a specialty appears after mapping
    # and was not in old_type_stats. This should not happen in duration_only mode.
    for col in ["mu_icu", "sd_icu", "mu_ward", "sd_ward"]:
        if col not in merged.columns:
            # Hard fallback to simple positive values. These are only used to
            # generate current bed states if old type_stats is malformed.
            if col.startswith("mu_"):
                merged[col] = 1.0
            else:
                merged[col] = 0.5
        else:
            merged[col] = merged[col].fillna(merged[col].dropna().mean() if merged[col].notna().any() else 1.0)

    return merged


def inject_mannino_pool(
    inst: Dict[str, Any],
    pool: pd.DataFrame,
    mode: str = "duration_only",
    team_map: Optional[Dict[str, str]] = None,
    override_priority: bool = False,
) -> Dict[str, Any]:
    """Inject Mannino durations, optionally mapping team to existing specialties."""
    inst = dict(inst)
    surgeries = inst["surgeries"].copy().sort_values("patient_id").reset_index(drop=True)
    blocks = inst["blocks"].copy()
    old_type_stats = inst.get("type_stats", pd.DataFrame()).copy()

    if len(surgeries) != len(pool):
        raise ValueError(f"surgeries has {len(surgeries)} rows, pool has {len(pool)} rows.")

    # Always replace duration using Mannino empirical duration.
    surgeries["duration_min"] = pool["duration_min"].astype(float).round(3)
    surgeries["mannino_team"] = pool["team"].astype(str).values

    if mode == "mapped_specialty":
        if team_map is None:
            team_map = _auto_team_map(pool, blocks)
        surgeries["original_specialty"] = surgeries["specialty"].astype(str)
        surgeries["specialty"] = pool["team"].astype(str).map(team_map).fillna(surgeries["specialty"]).values

        surgeries["compatible_block_ids"] = surgeries["specialty"].apply(
            lambda s: _compatible_block_string(blocks, str(s))
        )
        bad = surgeries["compatible_block_ids"].astype(str).str.len() == 0
        if bad.any():
            bad_specs = sorted(surgeries.loc[bad, "specialty"].astype(str).unique().tolist())
            raise ValueError(
                "Some mapped specialties have no compatible blocks: "
                + ", ".join(bad_specs)
            )

    elif mode != "duration_only":
        raise ValueError("mode must be duration_only or mapped_specialty")

    inst["surgeries"] = surgeries

    pr = inst.get("patient_priority")
    if isinstance(pr, pd.DataFrame) and not pr.empty:
        pr = pr.copy()
        m = surgeries[["patient_id", "surgery_id", "specialty"]].copy()
        pr = pr.drop(columns=["specialty"], errors="ignore").merge(
            m, on=["patient_id", "surgery_id"], how="left"
        )

        if override_priority and "priority_class" in pool.columns:
            priority_map = {
                "high": ("high", 3.0),
                "normal": ("medium", 2.0),
                "medium": ("medium", 2.0),
                "low": ("low", 1.0),
            }
            pr = pr.sort_values("patient_id").reset_index(drop=True)
            classes, scores = [], []
            for x in pool["priority_class"].astype(str).str.lower():
                c, s = priority_map.get(x, ("medium", 2.0))
                classes.append(c)
                scores.append(s)
            pr["priority_class"] = classes
            pr["priority_score"] = scores

        inst["patient_priority"] = pr

    # CRITICAL FIX: preserve mu_icu / mu_ward and other downstream columns.
    inst["type_stats"] = _refresh_type_stats_preserve_downstream(inst, old_type_stats)

    meta = dict(inst.get("metadata", {}))
    meta["mannino_duration_source"] = "SINTEF MSS-Adjusts Surgery Data"
    meta["mannino_injection_mode"] = mode
    if team_map is not None:
        meta["mannino_team_map"] = team_map
    inst["metadata"] = meta

    return inst


def apply_scenario(inst: Dict[str, Any], scenario: str) -> Dict[str, Any]:
    inst = dict(inst)
    caps = inst["capacities"].copy()

    if scenario == "nominal":
        pass

    elif scenario == "ward_pressure":
        caps["ward_capacity"] = (
            caps["ward_capacity"].astype(float)
            .mul(0.90)
            .apply(math.floor)
            .clip(lower=1)
            .astype(int)
        )

    elif scenario == "transfer_bottleneck":
        mask = caps["day_index"].astype(int).isin([3, 4, 5])
        caps.loc[mask, "ward_capacity"] = (
            caps.loc[mask, "ward_capacity"].astype(float)
            .mul(0.75)
            .apply(math.floor)
            .clip(lower=1)
            .astype(int)
        )

    else:
        raise ValueError(f"Unknown scenario: {scenario}")

    inst["capacities"] = caps
    meta = dict(inst.get("metadata", {}))
    meta["scenario"] = scenario
    if scenario == "ward_pressure":
        meta["scenario_description"] = "Ward capacity reduced by 10% across the horizon."
    elif scenario == "transfer_bottleneck":
        meta["scenario_description"] = "Ward capacity reduced by 25% on days 3--5."
    else:
        meta["scenario_description"] = "Baseline capacity setting."
    inst["metadata"] = meta
    return inst


def generate_one(
    n: int,
    seed: int,
    scenario: str,
    pool_dir: Path,
    output_dir: Path,
    mode: str,
    team_map: Optional[Dict[str, str]],
    override_priority: bool,
    n_scenarios: int,
    priority_policy: str = PUBLICATION_PRIORITY_POLICY,
    priority_shares: Optional[Dict[str, float]] = None,
    hepatobiliary_proxy: bool = PUBLICATION_HEPATOBILIARY_PROXY,
) -> Dict[str, Any]:
    bed_setting = PUBLICATION_BED_SETTINGS.get(n, (max(6, round(0.14 * n)), max(5, round(0.10 * n))))

    cfg = BenchmarkConfig(
        dataset="case",
        I=int(n),
        seed=int(seed),
        bed_setting=bed_setting,
        cost_structure=2,
        distribution="lognormal",
    )

    inst = generate_benchmark(cfg)

    pool = _read_pool(pool_dir, n, seed)
    inst = inject_mannino_pool(
        inst=inst,
        pool=pool,
        mode=mode,
        team_map=team_map,
        override_priority=override_priority,
    )

    inst = extend_instance_with_blocking_states(
        inst,
        seed=int(seed) + 70,
        initial_icu_occupancy_rate=0.80,
        initial_ward_occupancy_rate=0.90,
        ready_fraction_among_icu=0.50,
        schedule_priority="patient_id",
        transfer_priority="current_first",
    )

    inst = extend_instance_with_synthetic_inputs(
        inst,
        seed=int(seed) + 80,
        n_scenarios=int(n_scenarios),
        priority_policy=priority_policy,
        priority_shares=priority_shares or PUBLICATION_PRIORITY_SHARES,
        hepatobiliary_proxy=hepatobiliary_proxy,
    )

    inst = apply_scenario(inst, scenario)
    inst["metadata"] = dict(inst.get("metadata", {}))
    inst["metadata"].update({
        "dataset_family": "Mannino",
        "base_instance_id": f"Mannino_n{int(n)}_seed{int(seed)}",
        "priority_policy": priority_policy,
        "priority_shares": priority_shares or PUBLICATION_PRIORITY_SHARES,
        "hepatobiliary_proxy": bool(hepatobiliary_proxy),
        "publication_experiment_version": PUBLICATION_EXPERIMENT_VERSION,
        "scenario_role": "primary" if scenario in ["nominal", "transfer_bottleneck"] else "sensitivity",
    })

    output_dir.mkdir(parents=True, exist_ok=True)
    save_instance(inst, output_dir)

    report = run_extended_sanity_checks(inst)
    with open(output_dir / "sanity_report.json", "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, default=str)

    meta = {
        "n": int(n),
        "seed": int(seed),
        "scenario": scenario,
        "bed_setting": bed_setting,
        "mannino_pool_file": str(pool_dir / f"mannino_pool_n{n}_seed{seed}.csv"),
        "mannino_mode": mode,
        "output_dir": str(output_dir),
        "dataset_family": "Mannino",
        "base_instance_id": "Mannino_n{int(n)}_seed{int(seed)}",
        "priority_policy": priority_policy,
        "priority_shares": priority_shares or PUBLICATION_PRIORITY_SHARES,
        "hepatobiliary_proxy": bool(hepatobiliary_proxy),
        "publication_experiment_version": PUBLICATION_EXPERIMENT_VERSION,
    }
    with open(output_dir / "mannino_instance_metadata.json", "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2, default=str)

    return meta


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate Mannino-based OR--ICU--ward benchmark instances.")
    parser.add_argument("--mannino-pool-dir", required=True)
    parser.add_argument("--output-root", default="mannino_publication_batch_instances")
    parser.add_argument("--sizes", nargs="+", type=int, default=PUBLICATION_SIZES)
    parser.add_argument("--seeds", nargs="+", type=int, default=PUBLICATION_SEEDS)
    parser.add_argument("--scenarios", nargs="+", default=PUBLICATION_SCENARIOS)
    parser.add_argument("--mode", choices=["duration_only", "mapped_specialty"], default="duration_only")
    parser.add_argument("--team-map-json", default=None)
    parser.add_argument("--override-priority", action="store_true")
    parser.add_argument("--priority-policy", choices=["independent_urgency", "legacy_acuity_linked"], default=PUBLICATION_PRIORITY_POLICY)
    parser.add_argument("--priority-shares-json", default=None, help="Optional JSON file with high/medium/low shares.")
    parser.add_argument("--hepatobiliary-proxy", action="store_true", help="Legacy sensitivity only; disabled by default for publication instances.")
    parser.add_argument("--n-scenarios", type=int, default=50)
    args = parser.parse_args()

    pool_dir = Path(args.mannino_pool_dir)
    root = Path(args.output_root)
    root.mkdir(parents=True, exist_ok=True)

    priority_shares = dict(PUBLICATION_PRIORITY_SHARES)
    if args.priority_shares_json:
        priority_shares = json.loads(Path(args.priority_shares_json).read_text(encoding="utf-8"))

    team_map = None
    if args.team_map_json:
        team_map = json.loads(Path(args.team_map_json).read_text(encoding="utf-8"))

    rows = []
    for scenario in args.scenarios:
        for n in args.sizes:
            for seed in args.seeds:
                out = root / scenario / f"case_{n}_seed{seed}"
                print(f"\n=== Generating Mannino instance: {out} ===")
                meta = generate_one(
                    n=n,
                    seed=seed,
                    scenario=scenario,
                    pool_dir=pool_dir,
                    output_dir=out,
                    mode=args.mode,
                    team_map=team_map,
                    override_priority=args.override_priority,
                    n_scenarios=args.n_scenarios,
                    priority_policy=args.priority_policy,
                    priority_shares=priority_shares,
                    hepatobiliary_proxy=bool(args.hepatobiliary_proxy),
                )
                rows.append(meta)

    pd.DataFrame(rows).to_csv(root / "mannino_batch_instance_index.csv", index=False)
    print(f"\nGenerated {len(rows)} Mannino-based instances under {root}")
    print(f"Index: {root / 'mannino_batch_instance_index.csv'}")


if __name__ == "__main__":
    main()
