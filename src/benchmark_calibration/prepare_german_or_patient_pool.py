#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
prepare_german_or_patient_pool.py

Generate patient-pool CSV files from the German OR Benchmarking surgical
process data collection.

The script uses:
  - Case Mix
  - main_distributions
  - optionally OR cleaning_distributions

Default output:
  german_or_prepared/german_pool_n50_seed7.csv
  german_or_prepared/german_pool_n70_seed7.csv
  ...

These pool files can then be injected into the existing OR--ICU--ward instance
generator by batch_generate_german_publication_instances.py.

Recommended command:

python prepare_german_or_patient_pool.py ^
  --excel Surgical_process_data_collection_Germany_2019.xlsx ^
  --output-dir german_or_prepared ^
  --sizes 50 70 ^
  --seeds 7 11 19 23 29 31 37 41 43 47 ^
  --patient-types inpatient ^
  --duration-mode full_or

Duration modes:
  incision_only
      Incision-to-closure only.
  full_or
      AN induction + Surgical Lead-in + Incision-to-closure + Surgical Lead-out + AN emergence.
  full_or_plus_cleaning
      full_or + OR cleaning. Use only if you explicitly want turnover/cleaning in patient duration.

Important interpretation:
The German data calibrate OR-side process-time and case-mix distributions.
They do not provide ICU LOS, ward LOS, priority class, or ICU-to-ward transfer
records. Downstream parameters remain generated by the existing benchmark
procedure in the next step.
"""

from __future__ import annotations

import argparse
import json
import math
import re
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Tuple

import numpy as np
import pandas as pd


FULL_OR_COMPONENTS = [
    "AN induction",
    "Surgical Lead-in",
    "Incision-to-closure",
    "Surgical Lead-out",
    "AN emergence",
]


def norm_col(c: str) -> str:
    return re.sub(r"[^a-z0-9]+", "_", str(c).lower()).strip("_")


def clean_text(x) -> str:
    if pd.isna(x):
        return ""
    return str(x).strip()


def read_german_excel(path: Path) -> Tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    case_mix = pd.read_excel(path, sheet_name="Case Mix")
    main_dist = pd.read_excel(path, sheet_name="main_distributions")
    try:
        cleaning_dist = pd.read_excel(path, sheet_name="OR cleaning_distributions")
    except Exception:
        cleaning_dist = pd.DataFrame()

    case_mix = case_mix.rename(columns={c: norm_col(c) for c in case_mix.columns})
    main_dist = main_dist.rename(columns={c: norm_col(c) for c in main_dist.columns})
    cleaning_dist = cleaning_dist.rename(columns={c: norm_col(c) for c in cleaning_dist.columns})

    # Harmonize expected column names.
    rename_map = {
        "class_size_number_of_observations": "class_size",
        "main_ops_code": "ops_code",
    }
    case_mix = case_mix.rename(columns=rename_map)
    main_dist = main_dist.rename(columns={"main_ops_code": "ops_code"})

    for df in [case_mix, main_dist, cleaning_dist]:
        for c in ["hospital_loc", "surgical_specialty", "type_of_surgical_patient", "ops_code", "process_time", "distribution_type"]:
            if c in df.columns:
                df[c] = df[c].map(clean_text)

    if "class_size" in case_mix.columns:
        case_mix["class_size"] = pd.to_numeric(case_mix["class_size"], errors="coerce").fillna(0).astype(float)

    for df in [main_dist, cleaning_dist]:
        for c in ["parameter1", "parameter2", "expval", "var"]:
            if c in df.columns:
                df[c] = pd.to_numeric(df[c], errors="coerce")

    return case_mix, main_dist, cleaning_dist


def sample_distribution(row: pd.Series, rng: np.random.Generator) -> float:
    dist = str(row.get("distribution_type", "")).strip().lower()
    p1 = row.get("parameter1", np.nan)
    p2 = row.get("parameter2", np.nan)
    expval = row.get("expval", np.nan)

    try:
        p1 = float(p1)
        p2 = float(p2)
    except Exception:
        p1, p2 = np.nan, np.nan

    if not np.isfinite(p1) or not np.isfinite(p2):
        if np.isfinite(expval):
            return float(expval)
        return 0.0

    if dist == "lognormal":
        val = rng.lognormal(mean=p1, sigma=max(0.0, p2))
    elif dist == "gamma":
        val = rng.gamma(shape=max(1e-9, p1), scale=max(1e-9, p2))
    elif dist in {"weibull", "weibull_min"}:
        val = rng.weibull(a=max(1e-9, p1)) * max(1e-9, p2)
    else:
        # Conservative fallback to expected value.
        val = float(expval) if np.isfinite(expval) else 0.0

    if not np.isfinite(val):
        val = float(expval) if np.isfinite(expval) else 0.0
    return float(val)


def filter_case_mix(case_mix: pd.DataFrame, patient_types: List[str], locs: Optional[List[str]], specialties: Optional[List[str]]) -> pd.DataFrame:
    df = case_mix.copy()
    if patient_types:
        allowed = {x.lower() for x in patient_types}
        df = df[df["type_of_surgical_patient"].str.lower().isin(allowed)]
    if locs:
        allowed = {x.lower() for x in locs}
        df = df[df["hospital_loc"].str.lower().isin(allowed)]
    if specialties:
        allowed = {x.lower() for x in specialties}
        df = df[df["surgical_specialty"].str.lower().isin(allowed)]
    df = df[df["class_size"] > 0].copy()
    if df.empty:
        raise ValueError("Filtered German Case Mix is empty. Relax --patient-types/--locs/--specialties.")
    df["sample_weight"] = df["class_size"] / df["class_size"].sum()
    return df.reset_index(drop=True)


def match_distribution(
    dist: pd.DataFrame,
    loc: str,
    specialty: str,
    patient_type: str,
    ops_code: str,
    process_time: Optional[str],
    rng: np.random.Generator,
) -> Tuple[pd.Series, str]:
    """Find a distribution row using exact and fallback keys."""
    df = dist
    if df.empty:
        raise ValueError("distribution table is empty")

    def pick(sub: pd.DataFrame, reason: str) -> Tuple[pd.Series, str]:
        if sub.empty:
            raise KeyError
        # If multiple rows remain, choose one deterministically/randomly to avoid bias from sheet ordering.
        idx = int(rng.integers(0, len(sub)))
        return sub.iloc[idx], reason

    if process_time is not None and "process_time" in df.columns:
        base = df[df["process_time"].astype(str) == str(process_time)]
    else:
        base = df

    candidates = [
        ((base["hospital_loc"].eq(loc)) & (base["surgical_specialty"].eq(specialty)) & (base["type_of_surgical_patient"].eq(patient_type)) & (base.get("ops_code", pd.Series("", index=base.index)).eq(ops_code)), "exact_loc_specialty_type_ops"),
        ((base["hospital_loc"].eq(loc)) & (base["surgical_specialty"].eq(specialty)) & (base["type_of_surgical_patient"].eq(patient_type)), "fallback_loc_specialty_type"),
        ((base["surgical_specialty"].eq(specialty)) & (base["type_of_surgical_patient"].eq(patient_type)), "fallback_specialty_type"),
        ((base["surgical_specialty"].eq(specialty)), "fallback_specialty"),
    ]

    for mask, reason in candidates:
        sub = base[mask]
        if not sub.empty:
            return pick(sub, reason)

    return pick(base, "fallback_global_process_time" if process_time is not None else "fallback_global")


def sample_component(
    main_dist: pd.DataFrame,
    loc: str,
    specialty: str,
    patient_type: str,
    ops_code: str,
    process_time: str,
    rng: np.random.Generator,
    max_component_min: float,
) -> Tuple[float, str]:
    row, reason = match_distribution(main_dist, loc, specialty, patient_type, ops_code, process_time, rng)
    val = sample_distribution(row, rng)
    val = float(np.clip(val, 0.0, max_component_min))
    return val, reason


def sample_cleaning(
    cleaning_dist: pd.DataFrame,
    loc: str,
    specialty: str,
    patient_type: str,
    rng: np.random.Generator,
    max_component_min: float,
) -> Tuple[float, str]:
    if cleaning_dist.empty:
        return 0.0, "no_cleaning_table"
    row, reason = match_distribution(cleaning_dist, loc, specialty, patient_type, "", None, rng)
    val = sample_distribution(row, rng)
    val = float(np.clip(val, 0.0, max_component_min))
    return val, reason


def generate_pool(
    case_mix: pd.DataFrame,
    main_dist: pd.DataFrame,
    cleaning_dist: pd.DataFrame,
    n: int,
    seed: int,
    duration_mode: str,
    min_duration: float,
    max_duration: float,
    max_component_min: float,
) -> Tuple[pd.DataFrame, pd.DataFrame]:
    rng = np.random.default_rng(int(seed))
    idx = rng.choice(case_mix.index.to_numpy(), size=int(n), replace=True, p=case_mix["sample_weight"].to_numpy())
    sampled = case_mix.loc[idx].reset_index(drop=True)

    rows = []
    fallback_rows = []
    for k, cls in sampled.iterrows():
        loc = clean_text(cls["hospital_loc"])
        spec = clean_text(cls["surgical_specialty"])
        ptype = clean_text(cls["type_of_surgical_patient"])
        ops = clean_text(cls["ops_code"])

        comps: Dict[str, float] = {}
        reasons: Dict[str, str] = {}

        for comp in FULL_OR_COMPONENTS:
            val, reason = sample_component(main_dist, loc, spec, ptype, ops, comp, rng, max_component_min=max_component_min)
            comps[comp] = val
            reasons[comp] = reason
            if reason != "exact_loc_specialty_type_ops":
                fallback_rows.append({
                    "pool_row": k + 1,
                    "hospital_loc": loc,
                    "surgical_specialty": spec,
                    "patient_type": ptype,
                    "ops_code": ops,
                    "process_time": comp,
                    "fallback_reason": reason,
                })

        cleaning_val, cleaning_reason = sample_cleaning(cleaning_dist, loc, spec, ptype, rng, max_component_min=max_component_min)
        if cleaning_reason not in {"exact_loc_specialty_type_ops", "fallback_loc_specialty_type"}:
            fallback_rows.append({
                "pool_row": k + 1,
                "hospital_loc": loc,
                "surgical_specialty": spec,
                "patient_type": ptype,
                "ops_code": ops,
                "process_time": "OR cleaning",
                "fallback_reason": cleaning_reason,
            })

        incision = comps["Incision-to-closure"]
        full_or = sum(comps[c] for c in FULL_OR_COMPONENTS)

        if duration_mode == "incision_only":
            duration = incision
        elif duration_mode == "full_or":
            duration = full_or
        elif duration_mode == "full_or_plus_cleaning":
            duration = full_or + cleaning_val
        else:
            raise ValueError(f"Unknown duration_mode: {duration_mode}")

        duration = float(np.clip(duration, min_duration, max_duration))

        rows.append({
            "pool_id": k + 1,
            "team": spec,
            "duration_min": round(duration, 3),
            "duration_mode": duration_mode,
            "german_hospital_loc": loc,
            "german_surgical_specialty": spec,
            "german_patient_type": ptype,
            "german_ops_code": ops,
            "german_class_size": float(cls["class_size"]),
            "an_induction_min": round(comps["AN induction"], 3),
            "surgical_lead_in_min": round(comps["Surgical Lead-in"], 3),
            "incision_to_closure_min": round(comps["Incision-to-closure"], 3),
            "surgical_lead_out_min": round(comps["Surgical Lead-out"], 3),
            "an_emergence_min": round(comps["AN emergence"], 3),
            "full_or_process_min": round(full_or, 3),
            "or_cleaning_min": round(cleaning_val, 3),
            "fallback_count_main_components": sum(1 for x in reasons.values() if x != "exact_loc_specialty_type_ops"),
        })

    return pd.DataFrame(rows), pd.DataFrame(fallback_rows)


def main() -> None:
    parser = argparse.ArgumentParser(description="Prepare German OR process-time patient pools.")
    parser.add_argument("--excel", required=True, help="Surgical_process_data_collection_Germany_2019.xlsx")
    parser.add_argument("--output-dir", default="german_or_prepared")
    parser.add_argument("--sizes", nargs="+", type=int, default=[50, 70])
    parser.add_argument("--seeds", nargs="+", type=int, default=[7, 11, 19, 23, 29, 31, 37, 41, 43, 47])
    parser.add_argument("--patient-types", nargs="+", default=["inpatient"], help="Usually inpatient for OR--ICU--ward relevance.")
    parser.add_argument("--locs", nargs="*", default=None, help="Optional LOC filter.")
    parser.add_argument("--specialties", nargs="*", default=None, help="Optional surgical specialty filter.")
    parser.add_argument("--duration-mode", choices=["incision_only", "full_or", "full_or_plus_cleaning"], default="full_or")
    parser.add_argument("--min-duration", type=float, default=5.0)
    parser.add_argument("--max-duration", type=float, default=600.0)
    parser.add_argument("--max-component-min", type=float, default=300.0)
    args = parser.parse_args()

    excel = Path(args.excel)
    outdir = Path(args.output_dir)
    outdir.mkdir(parents=True, exist_ok=True)

    case_mix, main_dist, cleaning_dist = read_german_excel(excel)
    filtered = filter_case_mix(case_mix, args.patient_types, args.locs, args.specialties)

    all_meta = []
    all_fallback = []
    for n in args.sizes:
        for seed in args.seeds:
            pool, fallback = generate_pool(
                case_mix=filtered,
                main_dist=main_dist,
                cleaning_dist=cleaning_dist,
                n=int(n),
                seed=int(seed),
                duration_mode=args.duration_mode,
                min_duration=args.min_duration,
                max_duration=args.max_duration,
                max_component_min=args.max_component_min,
            )
            pool_file = outdir / f"german_pool_n{n}_seed{seed}.csv"
            pool.to_csv(pool_file, index=False)
            if not fallback.empty:
                fallback["n"] = int(n)
                fallback["seed"] = int(seed)
                all_fallback.append(fallback)

            all_meta.append({
                "n": int(n),
                "seed": int(seed),
                "pool_file": str(pool_file),
                "duration_mode": args.duration_mode,
                "patient_types": ";".join(args.patient_types),
                "mean_duration_min": float(pool["duration_min"].mean()),
                "sd_duration_min": float(pool["duration_min"].std(ddof=1)) if len(pool) > 1 else 0.0,
                "median_duration_min": float(pool["duration_min"].median()),
                "min_duration_min": float(pool["duration_min"].min()),
                "max_duration_min": float(pool["duration_min"].max()),
                "num_unique_german_specialties": int(pool["german_surgical_specialty"].nunique()),
                "num_unique_ops_codes": int(pool["german_ops_code"].nunique()),
                "num_rows_with_component_fallback": int((pool["fallback_count_main_components"] > 0).sum()),
            })
            print(f"Wrote {pool_file} | mean duration={pool['duration_min'].mean():.1f} min")

    index = pd.DataFrame(all_meta)
    index.to_csv(outdir / "german_pool_index.csv", index=False)
    if all_fallback:
        pd.concat(all_fallback, ignore_index=True).to_csv(outdir / "german_pool_fallback_log.csv", index=False)
    else:
        pd.DataFrame(columns=["n", "seed", "pool_row", "process_time", "fallback_reason"]).to_csv(outdir / "german_pool_fallback_log.csv", index=False)

    dataset_summary = {
        "source_file": str(excel),
        "case_mix_rows_total": int(len(case_mix)),
        "case_mix_rows_after_filter": int(len(filtered)),
        "case_mix_observations_after_filter": float(filtered["class_size"].sum()),
        "patient_types": args.patient_types,
        "duration_mode": args.duration_mode,
        "duration_definition": {
            "incision_only": "Incision-to-closure",
            "full_or": "AN induction + Surgical Lead-in + Incision-to-closure + Surgical Lead-out + AN emergence",
            "full_or_plus_cleaning": "full_or + OR cleaning",
        }[args.duration_mode],
    }
    (outdir / "german_pool_generation_metadata.json").write_text(json.dumps(dataset_summary, indent=2), encoding="utf-8")
    print(f"\nDone. Index: {outdir / 'german_pool_index.csv'}")


if __name__ == "__main__":
    main()
